'use strict';

describe('Directive: distribution', function () {
  beforeEach(module('askApp'));

  var element;

  it('should make hidden element visible', inject(function ($rootScope, $compile) {
    element = angular.element('<distribution></distribution>');
    element = $compile(element)($rootScope);
    expect(element.text()).toBe('this is the distribution directive');
  }));
});
